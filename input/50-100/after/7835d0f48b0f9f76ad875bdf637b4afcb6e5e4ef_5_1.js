function(number) {
		switch(number) {
			case 0:
				return 'MONDAY';
				break;
			case 1:
				return 'TUESDAY';
				break;
			case 2:
				return 'WEDNESDAY';
				break;
			case 3:
				return 'THURSDAY';
				break;
			case 4:
				return 'FRIDAY';
				break;
			case 5:
				return 'SATURDAY';
				break;
			case 6:
				return 'SUNDAY';
				break;
		}
	}