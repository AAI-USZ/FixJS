function(){
	if(this.AI === 'normal'){
		var distanceX = WIDTH/2 - (this.x - characterInfo.x);
		var distanceY = HEIGHT/2 - (this.y - characterInfo.y);
		if(distanceX < 10 && distanceX > -10){
			this.AIxy = 'y';
		}else if(distanceY < 10 && distanceY > -10){
			this.AIxy = 'x';
		};
		if(this.AIxy === 'x'){
			if(distanceX > 0){
				this.faceEast();
			}else{
				this.faceWest();
			};
		}else{
			if(distanceY < 0){
				this.faceNorth();
			}else{
				this.faceSouth();
			};
		};
		this.walk();
	};
}